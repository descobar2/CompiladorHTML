    public String getTokenName(Symbol symbol) {
        try {
            for (Field field : sym.class.getFields()) {
                if (field.getType() == int.class && field.getInt(null) == symbol.sym) {
                    return field.getName();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
        return "SIMBOLO_DESCONOCIDO_" + symbol.sym;
    }

